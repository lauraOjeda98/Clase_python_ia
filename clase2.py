# Condicionales
# Tabla de verdad

# Tabla del and
# v and v = v
# v and f = f
# f and v = f
# f and f = f

print(True and True)
print(True and False)
print(False and True)
print(False and False)

# Table del or
# v or v = v
# v or f = v
# f or v = v
# f or f = f

print(True or True)
print(True or False)
print(False or True)
print(False or False)
